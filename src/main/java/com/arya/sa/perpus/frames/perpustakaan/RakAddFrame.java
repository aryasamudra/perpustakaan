    public RakAddFrame(int id) {
        initComponents();
        try {
            Connection koneksi = Basisdata.getConnection();
            String findSQL = "SELECT * FROM rakbuku WHERE NoRak="+id;
            Statement statement = koneksi.createStatement();
            ResultSet resultSet = statement.executeQuery(findSQL);
            while(resultSet.next()){
                idTextField.setText(resultSet.getString("NoRak"));
                lokasiTextField.setText(resultSet.getString("Lokasi"));
                kapasitasTextField.setText(resultSet.getString("Kapasitas"));              
            }
            koneksi.close();
        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        }
    }
