    private void simpanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanButtonActionPerformed
        if (idTextField.getText().equals("")) {
            try {
                Connection koneksi = Basisdata.getConnection();
                String insertSQL = "INSERT INTO rakbuku SET " +
                "Lokasi = '" + lokasiTextField.getText() + "'," +
                "Kapasitas = '" + kapasitasTextField.getText() + "'";
                PreparedStatement statement = koneksi.prepareStatement(insertSQL);
                statement.executeUpdate();
                koneksi.close();
                dispose();
            } catch (SQLException ex) {
                System.err.println(ex.getMessage());
            }
        } else {
            try {
                Connection koneksi = Basisdata.getConnection();
                String updateSQL = "UPDATE rakbuku SET " +
                " Lokasi = '" + lokasiTextField.getText() + "'," +
                " Kapasitas = '" + kapasitasTextField.getText() + "'" +
                " WHERE NoRak=" + idTextField.getText();
                PreparedStatement statement = koneksi.prepareStatement(updateSQL);
                statement.executeUpdate();
                koneksi.close();
                dispose();
            } catch (SQLException ex) {
                System.err.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_simpanButtonActionPerformed

